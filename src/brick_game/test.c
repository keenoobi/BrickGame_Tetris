#include <ncurses.h>

int main() {
  // Инициализация ncurses
  initscr();
  cbreak();
  noecho();
  curs_set(0);

  // Определение массива для квадрата (4x4)
  int square[4][4] = {{0, 0, 1, 0}, {0, 0, 1, 0}, {0, 0, 1, 0}, {0, 1, 1, 0}};

  // Отображение квадрата на экране
  for (int i = 0; i < 4; i++) {
    for (int j = 0; j < 4; j++) {
      if (square[i][j] == 1) {
        mvaddch(i, j * 2,
                'X');  // Умножаем j на 2 для учета ширины символов в терминале
      }
    }
  }

  refresh();

  // Ожидание нажатия клавиши перед завершением программы
  getch();

  // Завершение работы с ncurses
  endwin();

  return 0;
}